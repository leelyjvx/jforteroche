<h2> Jean Forteroche </h2>

<p> Né le 20 mai 1972 à Amiens d'un père policier et d'une mère avocate, Jean Forteroche s'est trouvé une vocation dès sa plus tendre enfance pour la nature et l'environnement. Voyant peu à peu ce monde qu'il aimait tant se défaire peu à peu, il a choisi d'agir, à sa façon, afin d'améliorer les choses et de mettre un terme à ce qu'il considère comme un désastre imminent. </br>

En 1985, à 13 ans, il lance son association de protection de la nature et rassemble ue quarantaine de personnes pour diverses actions, telles que la sensibilisation publique via des distributions de flyers recyclables, la création de gobelets en bois, le ramassage des déchets, etc. </br>

En 1992, il part faire ses études en Suède et apprend le mode de vie de ce pays très penché sur l'écologie. Il se lance dans une carrière de politique, rentre en France et rejoint le mouvement Europe Ecologie Les Verts. </br>

Durant sa carrière, il écrit trois livres : "L'écologie tout terrain", "Manger moins de viande" et le best-seller "Sauvez la planète". Fort de ses expériences, il s'achète un bateau nommé "Le Poisson" et part naviguer avec un équipage de 15 personnes. Après une grave tempête, le bateau fait naufrage après avoir percuté un sac plastique et une bouteille de soda. Il est le seul à en sortir indemne. </br>

Rentré en France, il se remet à l'écriture, plus fort et determiné que jamais.

</p>

